== <b>Description</b> ==
<p>
Thank you for installing Banner Ad Slideshow. This plugin is all-in-one slider solution for WordPress that create responsive(Premium Features) 
banners. Please visit [demo](https://walexconcepts.com/wordpress/).
</p>


Free Version features:
<p>
<ul>
<li>* Use shortcode to add it to any page at frontend.</li>
<li>* Click 'Add new banner' under 'Banner Ad Slideshow' menu at admin page to insert banner.</li>
</ul>
</p>

Premium Features:
<p>
<ul>
<li>* All of the above free version features. </li>
<li>* Click 'Banner Settings' under 'Banner Ad Slideshow' menu at admin page for configuration.</li>
<li>* Banner Ad Slideshow looks responsive on any device.</li>
<li>* The Banner Ad Slideshow description added.</li>
<li>* The Banner Ad Slideshow URL added.</li>
<li>* The Banner Ad Slideshow Expiring date added.</li>
<li>* Click 'Banner Manager' under 'Banner Ad Slideshow' to Enable / Disable banner Status.</li>
<li>* Click 'Banner Manager' under 'Banner Ad Slideshow' to delete banner.</li>
<li>* Click 'Banner Manager' under 'Banner Ad Slideshow' to edit banner description, URL etc.</li>
<li>* No cron job require to automate banner expiring date</li>
</ul>
</p>
